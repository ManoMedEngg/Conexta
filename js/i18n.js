let currentLanguage =
  (typeof localStorage !== "undefined" &&
    localStorage.getItem(CONEXTA_I18N_KEY)) ||
  "en";

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLanguage = lang;
  try {
    localStorage.setItem(CONEXTA_I18N_KEY, lang);
  } catch (_) {
    // ignore
  }
  document.documentElement.lang = lang;
  const event = new CustomEvent("conexta:languageChanged", { detail: { lang } });
  window.dispatchEvent(event);
}

function t(key) {
  const langTable = translations[currentLanguage] || translations.en;
  return (
    (langTable && langTable[key]) ||
    (translations.en && translations.en[key]) ||
    key
  );
}

